# В Python условные операторы используются для принятия решений, в зависимости от выполнения или невыполнения определенных условий. 
# Основными условными операторами являются if, elif и else. Рассмотрим их использование и методы работы:

                                          # Оператор if
# Этот оператор позволяет проверить условие, и если оно истинно, выполнить блок кода.
x = 10
if x > 5:
    print("x больше 5") # Если условие x > 5 истинно, то выводится сообщение "x больше 5".

                                          # Оператор elif
# Если условие в блоке if не выполнено, можно использовать elif (сокращение от "else if") для проверки других условий.
x = 10
if x > 15:
    print("x больше 15")
elif x > 5:
    print("x больше 5, но меньше или равно 15") # В этом случае будет выведено "x больше 5, но меньше или равно 15", так как первое условие не сработало.

                                          # Оператор else
# Оператор else выполняет блок кода, если ни одно из предыдущих условий не выполнено.
x = 3
if x > 5:
    print("x больше 5")
else:
    print("x меньше или равно 5") # Здесь будет выведено "x меньше или равно 5".

                                           # Логические операторы
# Для комбинирования условий используются логические операторы:

# and — возвращает True, если оба условия истинны.
# or — возвращает True, если хотя бы одно условие истинно.
# not — инвертирует результат условия.
x = 10
y = 20
if x > 5 and y > 15:
    print("Оба условия истинны")

                                            # Методы и функции для условных операторов
# bool(): Преобразует объект в булево значение (True или False).  
print(bool(0))  # False
print(bool("Hello"))  # True

# any(): Возвращает True, если хотя бы один элемент в итерируемом объекте истинный.
print(any([False, True, False]))  # True

# all(): Возвращает True, если все элементы в итерируемом объекте истинны.
print(all([True, True, True]))  # True

                                            # Пример сложных условных операторов
x = 10
y = 20
z = 30

if x > 5 and y > 15:
    if z > 25:
        print("Все условия выполнены")
    else:
        print("z меньше или равно 25")
else:
    print("Первое условие не выполнено")

# Условные операторы позволяют эффективно контролировать поток выполнения программы в зависимости от выполнения определенных условий. 
# Эти операторы являются основой для принятия решений в программировании и используются во всех областях разработки.                                           

