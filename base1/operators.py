                                   # Арифметические операторы
#   Используются для выполнения математических операций:

#  Оператор	        Описание	           Пример	       Результат
#     +	            Сложение	            2 + 3	          5
#     -	            Вычитание	            5 - 2	          3
#     *	            Умножение	            2 * 3	          6
#     /	            Деление	                7 / 2	          3.5
#     //	        Целочисленное деление	7 // 2	          3
#     %	            Остаток от деления	    7 % 2	          1
#     **	        Возведение в степень	2 ** 3	          8

                                   # Операторы сравнения
#   Возвращают булевое значение (True или False):

#   Оператор	    Описание	            Пример	         Результат
#      ==	         Равно	                2 == 3	           False
#      !=	         Не равно	            2 != 3	           True
#      >	         Больше	                5 > 3	           True
#      <	         Меньше	                3 < 5	           True
#      >=	         Больше или равно	    5 >= 5	           True
#      <=	         Меньше или равно	    3 <= 5	           True

                                     # Логические операторы
#   Операции над булевыми значениями:

#    Оператор	     Описание	            Пример	           Результат
#      and	            И	               True and False	     False
#      or	            Или	               True or False	     True
#      not	            Не	               not True	             False

                                    # Операторы присваивания
#   Используются для присваивания значений переменным:

#    Оператор	      Описание	                Пример	            Результат
#       =	          Присваивание	             x = 5	             x = 5
#       +=	          Сложение и присвоение	     x += 3	             x = x + 3
#       -=	          Вычитание и присвоение     x -= 2	             x = x - 2
#       *=	          Умножение и присвоение	 x *= 2	             x = x * 2
#       /=	          Деление и присвоение	     x /= 3	             x = x / 3
#       //=	  Целочисленное деление и присвоение x //= 2	         x = x // 2
#       %=	          Остаток и присвоение	     x %= 2	             x = x % 2
#       **=	          Степень и присвоение	     x **= 2	         x = x ** 2

                                    # Операторы работы с битами
#   Применяются к целым числам на уровне битов:

#     Оператор	         Описание	             Пример	             Результат
#        &	             Побитовое И	          5 & 3	                1
#        `	                 `	                  Побитовое ИЛИ	        `5
#        ^	             Побитовое XOR	           5 ^ 3	             6
#        ~	             Побитовое НЕ	            ~5	                 -6
#        <<	             Сдвиг влево	           5 << 1	             10
#        >>	             Сдвиг вправо	           5 >> 1	             2

                                    # Операторы принадлежности
#    Проверяют наличие элемента в последовательности:

#      Оператор	         Описание	               Пример	            Результат
#        in	             Находится	              'a' in 'abc'	          True
#        not in	         Не находится	          'd' not in 'abc'	      True

                                    #  Операторы идентификации
#    Проверяют, ссылаются ли две переменные на один объект:

#       Оператор	      Описание	                Пример	             Результат
#          is	          Одинаковый объект	        x is y	              True/False
#         is not	      Разные объекты	        x is not y	          True/False


