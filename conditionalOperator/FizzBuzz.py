# В заключение рассмотрим задачу "FizzBuzz”. Задача часто встречается на собеседованиях по программированию. 
# Его суть состоит в написании программы, которая для заданного числа:



#Выводит "Fizz", если число кратно какому-либо определенному числу (например, 3);
#Вывод "Buzz", если число кратно другому определенному числу (например, 5);
#Выводит "FizzBuzz", если число кратно обоим этим числам;
#В противном случае выводит самое число.


#На первый взгляд это достаточно простая задача, но даже опытные программисты могут ошибиться. 
# Задача FizzBuzz часто считается базовым тестом на способность кандидата понимать и применять основные концепции программирования. 
# Хотя она может показаться простой, ее эффективное решение требует слаженного применения нескольких основных программирующих принципов.
# Задаємо конкретне число
num = int(input())

# Перевіряємо кратність
if num % 3 == 0 and num % 5 == 0:
    print("FizzBuzz")
elif num % 3 == 0:
    print("Fizz")
elif num % 5 == 0:
    print("Buzz")
else:
    print(num)

# Условие if num % 3 == 0 and num % 5 == 0 проверяет, введено ли число кратное и 3, и 5 одновременно. Эта проверка стоит первой, поскольку она наиболее специфична.
#Если это условие выполняется, выводится "FizzBuzz". Если бы эта проверка была после других, она бы никогда не выполнялась, потому что более общие условия (кратность 3 или 5) уже были бы выполнены. 
# Далее elif num % 3 == 0 проверяет, кратно ли только 3. Это условие выполняется только если предыдущее условие (кратность и 3, и 5) не выполнилось. Если это правда, выводится Fizz. 
# Условие elif num % 5 == 0 проверяет, кратно ли только 5. Как и предыдущее, это условие выполняется только тогда, когда предыдущие проверки не оказались истинными. 
# В случае выполнения этого условия выводится Buzz. Если ни одно из вышеперечисленных условий не выполнилось, то else гарантирует, что будет выведено само число.

#Порядок этих условий является ключевым, так как он обеспечивает, что самая специфическая проверка - кратность и 3, и 5 имеет приоритет, а затем проверяются менее специфические случаи - кратность отдельно 3 или 5. 
# Это предотвращает случаи, когда числа, которые кратны и 3, и 5 , были бы неправильно обработаны из-за выполнения менее специфических условий.

