'''
Метод __init__ в Python — это так называемый конструктор класса. Он автоматически вызывается при создании нового объекта класса и используется для инициализации его атрибутов.

Основные особенности __init__:
Инициализация объекта: Метод позволяет задать начальное состояние объекта, например, установить значения его атрибутов.
Автоматический вызов: Не нужно вызывать __init__ вручную — он вызывается автоматически, когда вы создаёте объект класса.
Сигнатура: Первый аргумент метода — всегда self, который ссылается на текущий экземпляр класса.
Синтаксис'''
class MyClass:
    def __init__(self, param1, param2):
        self.attr1 = param1
        self.attr2 = param2
